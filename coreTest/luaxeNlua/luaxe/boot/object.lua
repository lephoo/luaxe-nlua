-- base Object class
Object = {}
Object.__index = Object;
Object.__super__ = nil
function Object.__tostring(o)
    local s = "{ "
    function prv(n,v)
    	s = s + n + ": " + v
    end
    local first = true
    for key, value in pairs (o) do
    	prv(first and key or (", " + key),value)
    	first = false
    end	
    return s + " }"
end